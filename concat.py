# coding: UTF-8

import os
import subprocess

def main():
    movie_list = os.sys.argv[1:]
    command = [
        'ffmpeg', '-i',
        r'concat:"' + '|'.join(movie_list) + r'"',
        '-c:v', 'copy',
        '-c:a', 'copy',
        'concat_' + movie_list[0]
    ]
    p = subprocess.call(' '.join(command), shell=True)

if __name__ == '__main__':
    main()

